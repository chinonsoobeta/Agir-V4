// Generates self-contained demo-fixture modules from the binary fixtures in
// src/test/fixtures. Each fixture file is base64-embedded into a TypeScript
// module under src/lib/demo/fixtures so the in-app demo seeder ships the bytes
// in the JS bundle (no reliance on src/test/fixtures being present at runtime,
// which it is not in a serverless deploy). Re-run after changing a fixture:
//
//   node scripts/generate-demo-fixtures.mjs
//
import { readFile, readdir, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const fixturesDir = path.join(root, "src/test/fixtures");
const outDir = path.join(root, "src/lib/demo/fixtures");

// slug -> { dir, constName }. Only file-bearing demo packages are generated
// here; Harbour Centre renders its documents in code (harbour-demo-docs.ts).
const PACKAGES = [
  { dir: "rivergate", constName: "RIVERGATE_FIXTURE_FILES" },
  { dir: "summit-point", constName: "SUMMIT_POINT_FIXTURE_FILES" },
];

const mime = (name) =>
  name.endsWith(".pdf")
    ? "application/pdf"
    : name.endsWith(".xlsx")
      ? "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet"
      : name.endsWith(".xls")
        ? "application/vnd.ms-excel"
        : name.endsWith(".docx")
          ? "application/vnd.openxmlformats-officedocument.wordprocessingml.document"
          : "application/octet-stream";

await mkdir(outDir, { recursive: true });

for (const pkg of PACKAGES) {
  const dir = path.join(fixturesDir, pkg.dir);
  const names = (await readdir(dir))
    // Project_Facts.json is metadata about the test, not a source document.
    .filter((n) => /\.(pdf|xlsx|xls|docx)$/i.test(n))
    .sort();

  const entries = [];
  for (const name of names) {
    const bytes = await readFile(path.join(dir, name));
    entries.push(
      `  { name: ${JSON.stringify(name)}, fileType: ${JSON.stringify(mime(name))}, base64: ${JSON.stringify(bytes.toString("base64"))} },`,
    );
  }

  const body = `// AUTO-GENERATED by scripts/generate-demo-fixtures.mjs - do not edit by hand.
// Source: src/test/fixtures/${pkg.dir}
import type { DemoFixtureFile } from "./types";

export const ${pkg.constName}: DemoFixtureFile[] = [
${entries.join("\n")}
];
`;
  const outFile = path.join(outDir, `${pkg.dir}.generated.ts`);
  await writeFile(outFile, body);
  const kb = Math.round(body.length / 1024);
  console.log(`wrote ${path.relative(root, outFile)} (${names.length} files, ~${kb}KB)`);
}
